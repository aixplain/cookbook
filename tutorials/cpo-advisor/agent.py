# Reference module. This file shows how to create and use the agent, but it does
# not execute automatically.

import json
import urllib.request

from aixplain import Aixplain

AIXPLAIN_API_KEY = "..."  # https://studio.aixplain.com/settings/keys


def build_agent():
    aix = Aixplain(api_key=AIXPLAIN_API_KEY)

    # Define tools if needed.
    tools = []

    llm = aix.Model.get('openai/gpt-4o/openai')

    agent = aix.Agent(
        name='Cpo Advisor',
        description='Provides strategic product leadership advice, including product vision, portfolio analysis, PMF scoring, and organizational design recommendations.',
        instructions='You are a CPO Advisor.\n\n* Evaluate product strategies, PMF, portfolio health, and organizational structures.\n* Provide recommendations on Invest/Maintain/Kill decisions.\n* Score PMF across retention, engagement, satisfaction, and growth.\n* Suggest organizational improvements for product teams.\n* Output clear actionable insights in structured text format.',
        llm=llm,
        tools=tools,
        output_format='markdown',
        max_iterations=5,
        max_tokens=2000,
    ).save()
    return agent


def run_agent_example(agent):
    result = agent.run(
        'Summarize what this agent does and the best way to use it.',
        progress_format="logs",
        progress_verbosity=2,
    )
    print(result.data.output)
    print(result.data)  # Debug helper
    return result


def rest_example(agent):
    request = urllib.request.Request(
        f"https://platform-api.aixplain.com/sdk/agents/{agent.id}/run",
        data=json.dumps(
            {
                "query": 'Summarize what this agent does and the best way to use it.'
            }
        ).encode("utf-8"),
        headers={
            "x-api-key": AIXPLAIN_API_KEY,
            "Content-Type": "application/json",
        },
        method="POST",
    )

    with urllib.request.urlopen(request, timeout=60) as response:
        return json.loads(response.read().decode("utf-8"))
